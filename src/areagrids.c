char* generate_area_grid(int size)
{
	char *area_grid[size][size];
	for (int i = 0; i < size; i++)
	{
		for (int j = 0; j < size; j++)
		{
			*area_grid[i][j] = '*';
		}
	}
	return area_grid[size][size];
}

void show_grid(int size)
{
	for (int i = 0; i < size; i++)
	{
		for (int j = 0; j < size; j++)
		{
			printf("%d ", area_grid_city[i][j]);
		}
		printf("\n");
	}
}